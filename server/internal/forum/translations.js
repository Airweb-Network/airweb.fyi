function template(text, vars) {
  return String(text || '').replace(/\{(\w+)\}/g, (_, key) => (vars && vars[key] != null ? String(vars[key]) : ''));
}

function t(locale, key, vars) {
  const base = TEXT[locale] || TEXT.en;
  const text = base[key] || TEXT.en[key] || key;
  return template(text, vars);
}

function tagMeta(locale, slug, fallback) {
  const group = TAGS[locale] || TAGS.en;
  const hit = group[slug] || TAGS.en[slug];
  return {
    slug,
    label: hit ? hit[0] : (fallback && fallback.label) || slug,
    description: hit ? hit[1] : (fallback && fallback.description) || '',
  };
}

function formatTimeAgo(locale, ts) {
  if (!ts) return t(locale, 'justNow');
  const delta = Math.max(0, Date.now() - Number(ts));
  const minute = 60 * 1000;
  const hour = 60 * minute;
  const day = 24 * hour;
  if (delta < minute) return t(locale, 'justNow');
  if (delta < hour) return t(locale, 'minuteAgo', { count: Math.round(delta / minute) });
  if (delta < day) return t(locale, 'hourAgo', { count: Math.round(delta / hour) });
  if (delta < 7 * day) return t(locale, 'dayAgo', { count: Math.round(delta / day) });
  return new Date(Number(ts)).toLocaleDateString(locale, { month: 'short', day: 'numeric', year: 'numeric' });
}

function countLabel(locale, kind, count) {
  if (kind === 'answer') return t(locale, count === 1 ? 'answerOne' : 'answerOther', { count });
  if (kind === 'bump') return t(locale, count === 1 ? 'bumpOne' : 'bumpOther', { count });
  return String(count);
}

